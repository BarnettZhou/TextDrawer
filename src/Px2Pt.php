    /**
     *  将 px 值转换为 gd2 可用的 pt 值
     * @param $px
     * @return float|mixed
     */
    public static function switchPx2Pt($px)
    {
        if (key_exists($px, self::$px2pt)) {
            return self::$px2pt[$px];
        }

        return 0.8 * $px;
    }
